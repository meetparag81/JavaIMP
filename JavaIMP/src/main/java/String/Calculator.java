package String;

public class Calculator 
{
	

	public double Addition(double x, double y)
	{
		return x+y;
	}
	
	public double Subtraction(double x, double y)
	{
		return x-y;
		
	}
	public double Multipication(double x, double y)
	{
		return x*y;
	}
	
	public double Division(double x, double y)
	{
		return x/y;
	}
		
	
	
	


}

